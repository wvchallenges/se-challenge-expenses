package com.tannerrutgers.sechallenge.application.expenses;

import com.fasterxml.jackson.annotation.JsonIgnore;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import java.math.BigDecimal;
import java.time.LocalDate;

/**
 * JPA Entity mapping for employee expense table
 */
@Entity(name = "EMPLOYEE_EXPENSE")
class EmployeeExpenseEntity {

    @Id
    @GeneratedValue
    @JsonIgnore
    private Long id;

    @Column(nullable=false)
    private LocalDate date;

    @Column(nullable=false)
    private String category;

    @Column(nullable=false)
    private String employeeName;

    @Column(nullable=false)
    private String employeeAddress;

    @Column(nullable=false)
    private String expenseDescription;

    @Column(nullable=false)
    private BigDecimal preTaxAmount;

    @Column(nullable=false)
    private String taxName;

    @Column(nullable=false)
    private BigDecimal taxAmount;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public String getEmployeeAddress() {
        return employeeAddress;
    }

    public void setEmployeeAddress(String employeeAddress) {
        this.employeeAddress = employeeAddress;
    }

    public String getExpenseDescription() {
        return expenseDescription;
    }

    public void setExpenseDescription(String expenseDescription) {
        this.expenseDescription = expenseDescription;
    }

    public BigDecimal getPreTaxAmount() {
        return preTaxAmount;
    }

    public void setPreTaxAmount(BigDecimal preTaxAmount) {
        this.preTaxAmount = preTaxAmount;
    }

    public String getTaxName() {
        return taxName;
    }

    public void setTaxName(String taxName) {
        this.taxName = taxName;
    }

    public BigDecimal getTaxAmount() {
        return taxAmount;
    }

    public void setTaxAmount(BigDecimal taxAmount) {
        this.taxAmount = taxAmount;
    }
}
